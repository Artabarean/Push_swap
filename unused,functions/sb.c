#include "push_swap.h"

void	_sb(int *sb)
{
	int	temp;

	if (sb[0] == '\0' || sb[1] == '\0')
		return ;
	temp = sb[0];
	sb[0] = sb[1];
	sb[1] = temp;
	write(1, "sb\n", 3);
}
