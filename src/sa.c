#include "push_swap.h"

void	_sa(int *sa)
{
	int	temp;

	if (sa[0] == '\0' || sa[1] == '\0')
		return ;
	temp = sa[0];
	sa[0] = sa[1];
	sa[1] = temp;
	write(1, "sa\n", 3);
}
