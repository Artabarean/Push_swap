#include "push_swap.h"

void    ra(int *sa)
{
    int temp;
    int i;
    
    i = 0;
    if (!sa || sa[1] == '\0')
        return ;
    while (sa[i] != '\0')
      i++;
    i--;
    temp = sa[i];
    while (i > 0)
    {
        sa[i] = sa[i - 1];
        i--;
    }
    sa[0] = temp;
	write(1, "ra\n", 3);
}
