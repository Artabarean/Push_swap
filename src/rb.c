#include "push_swap.h"

void    rb(int *sb)
{
    int temp;
    int i;
    
    i = 0;
    if (!sb || sb[1] == '\0')
        return ;
    while (sb[i] != '\0')
      i++;
    i--;
    temp = sb[i];
    while (i > 0)
    {
        sb[i] = sb[i - 1];
        i--;
    }
    sb[0] = temp;
	write(1, "rb\n", 3);
}
